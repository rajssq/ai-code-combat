/**
 * Obtém a tradução para um usuário específico
 */
function getTranslation(userId, key) {
  const { userLanguagePreferences } = require("../storage/memory");
  const userLang = userLanguagePreferences.get(userId) || "en";

  return translations[userLang][key] || translations.en[key] || key;
}

/**
 * Obtém todas as traduções para um idioma
 */
function getTranslations(userId) {
  const { userLanguagePreferences } = require("../storage/memory");
  const userLang = userLanguagePreferences.get(userId) || "en";

  return translations[userLang];
}

/**
 * Define o idioma de um usuário
 */
function setUserLanguage(userId, language) {
  const { userLanguagePreferences } = require("../storage/memory");
  userLanguagePreferences.set(userId, language);
}

/**
 * Obtém o idioma atual do usuário
 */
function getUserLanguage(userId) {
  const { userLanguagePreferences } = require("../storage/memory");
  return userLanguagePreferences.get(userId) || "en";
}

module.exports = {
  getTranslation,
  getTranslations,
  setUserLanguage,
  getUserLanguage,
  translations,
};
